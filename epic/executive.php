<h2 class="header">Cartridge Coders</h2>
<p class="title">Executive Summary</p>
<br>
<p>This app will be a consignment website for the sale/exchange of game cartridges for old style gaming systems. It
	allows users to create an account, place items for sale/exchange, browse current items in the system, and be
	connected to items they may be looking for using a search function. It also allows for the purchase of said items
	using PayPal.
</p>