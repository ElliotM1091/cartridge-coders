
<p class="title">Use Case Seller</p>
<br>
<p>Robert Sullivan has some old game cartridges that he wants to get rid of. He is using a Windows 7 computer and he is looking around the internet for a way to sell them.</p>
<p>He goes to the Cartridge Coders site and looks around a couple minutes at the ones currently available for sale.  He decides to sell one of his and signs up using his PayPal account.</p>
<p>He fills out a form for the listing and decides to just sell one for now.  He signs out of the site and shuts down his computer.</p>

